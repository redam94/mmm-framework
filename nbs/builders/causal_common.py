def variant(sc, *, keep_controls=None, add_controls=None, roles=None, name=None):
    """A copy of a Scenario with a different *analyst choice* of controls.

    The world (spend, KPI, truth) is untouched — only what the modeler
    conditions on changes. ``roles`` maps control name -> CausalControlRole.
    """
    controls = sc.controls
    if keep_controls is not None:
        controls = controls[list(keep_controls)]
    controls = controls.copy()
    if add_controls:
        for k, v in add_controls.items():
            controls[k] = np.asarray(v, float)
    return dataclasses.replace(
        sc,
        name=name or sc.name,
        controls=controls,
        control_roles=roles if roles is not None else sc.control_roles,
    )


def scenario_for(key: str):
    """The analyst's dataset for each named fit (world x control choice)."""
    from mmm_framework.config import CausalControlRole

    if key in ("uc", "cc", "rc", "clean"):
        return world(
            {
                "uc": "unobserved_confounding",
                "cc": "confounding_controlled",
                "rc": "reverse_causality",
                "clean": "clean",
            }[key]
        )
    if key == "funnel_flat":
        # the brand-funnel world handed to a FLAT MMM (no mediators, surveys
        # ignored) — nb03's "wrong model first" baseline.
        return funnel_world()
    if key == "cc_unmarked":
        # the demand proxy present but left UNMARKED (default precision prior,
        # shrunk toward zero) — the half-closed back-door.
        return variant(
            world("confounding_controlled"), roles={}, name="cc_unmarked"
        )
    if key in ("real_causal", "real_bad"):
        sc = world("realistic")
        roles = {
            "category_demand": CausalControlRole.CONFOUNDER,
            "distribution": CausalControlRole.CONFOUNDER,
        }
        mediator = sc.notes["mediator"]  # brand_awareness
        if key == "real_causal":
            keep = [c for c in sc.controls.columns if c != mediator]
            return variant(sc, keep_controls=keep, roles=roles, name="real_causal")
        # the bad-control trap: the mediator goes in UNMARKED (as a naive
        # modeler would add it) — marking it MEDIATOR is refused at build time.
        return variant(sc, roles=roles, name="real_bad")
    if key in ("econ_naive", "econ_ind", "econ_ind_unmarked"):
        sc = world("economic_health")
        if key == "econ_naive":
            return sc
        from mmm_framework.config import CausalControlRole as _R

        inds = sc.notes["indicators"]  # {name: array}
        roles = {n: _R.CONFOUNDER for n in inds} if key == "econ_ind" else {}
        return variant(sc, add_controls=inds, roles=roles, name=key)
    raise KeyError(f"unknown fit key: {key}")


