"""Shared worlds / fits / truth machinery for the **Causal Inference in
Practice** notebook series (``causal_00`` .. ``causal_10``).

One synthetic brand — *Veranda Home* — walked up the **ladder of evidence**:

    00 the ladder          ->  01 confounding & adjustment
    02 the MMM as a causal model                (estimands, refutation)
    03 structural mediation   04 latent confounders     (structural learning)
    05 measuring one experiment                 (estimator methodology)
    06 calibrating the model  07 many experiments       (evidence composition)
    08 designing the next test                  (economics + identification)
    09 planning the measurement series          (EIG / EVOI / decay)
    10 the closed loop                          (converging on the truth)

The series' epistemic conceit: every notebook is a *possible hidden history*
of the same brand — identical-looking dashboards generated by different causal
worlds (``mmm_framework.synth``), each shipping a **sealed answer key** the
analyst never sees while modelling. Every claim a notebook makes is graded
against that key, and the grading itself is verified: :func:`check_truth`
re-derives each world's per-channel causal contribution from the DGP's own
response constants and asserts it matches the scenario's answer key to float
precision, so if the DGP ever drifts the bake fails loudly instead of silently
mis-grading.

Fit policy (mixed budget): observational MMMs are fast-but-real numpyro NUTS
posteriors (2 chains x 500 draws), fit **once per world** and cached to disk
(``MMMSerializer`` + a cloudpickled panel). The two structural notebooks
(mediation, latent factor) need genuine 4-chain NUTS to mix; they cache their
*derived* tables/arrays so re-bakes are seconds. Delete the cache dir (or set
``MMM_CAUSAL_CACHE``) to force a clean re-fit.

Convention (matches ``lifecycle_common.py``): a plain module imported by the
baked notebooks; bake with ``PYTHONPATH=..`` from ``nbs/`` so both this module
and the installed ``mmm_framework`` resolve. Kernel cwd is ``nbs/causal/``.
"""

