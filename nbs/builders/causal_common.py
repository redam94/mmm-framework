def truth_forest(g: pd.DataFrame, *, roas: bool = False, title: str | None = None,
                 height: int = 420):
    """The series' recurring grade chart: posterior interval dots per channel
    with the sealed truth as near-black ticks. ``g`` is a :func:`grade` table."""
    import plotly.graph_objects as go

    est, lo, hi, tr = ("est_roas", "roas_lo", "roas_hi", "true_roas") if roas else (
        "est", "lo", "hi", "true")
    channels = list(g.index)
    fig = go.Figure()
    for i, c in enumerate(channels):
        fig.add_trace(go.Scatter(
            x=[g.loc[c, lo], g.loc[c, hi]], y=[i, i], mode="lines",
            line=dict(color=PALETTE.get(c, MUTED), width=5), opacity=0.45,
            showlegend=False, hoverinfo="skip"))
        fig.add_trace(go.Scatter(
            x=[g.loc[c, est]], y=[i], mode="markers",
            marker=dict(color=PALETTE.get(c, MUTED), size=12),
            name="estimate (90% HDI)", showlegend=(i == 0),
            hovertemplate=f"{c}: %{{x:.2f}}<extra>estimate</extra>"))
        fig.add_trace(go.Scatter(
            x=[g.loc[c, tr]], y=[i], mode="markers",
            marker=dict(color=TRUTH, symbol="line-ns-open", size=18,
                        line=dict(width=3)),
            name="sealed truth", showlegend=(i == 0),
            hovertemplate=f"{c}: %{{x:.2f}}<extra>truth</extra>"))
    fig.update_yaxes(tickvals=list(range(len(channels))), ticktext=channels,
                     autorange="reversed")
    fig.update_xaxes(title="ROAS (KPI $ per media $)" if roas
                     else "total contribution (KPI units)")
    return style(fig, height=height, title=title)


def dag(nodes: dict, edges: list, *, bad_edges: list = (), title: str | None = None,
        height: int = 380, node_colors: dict | None = None):
    """A small plotly DAG: ``nodes`` maps name -> (x, y); ``edges`` is a list of
    (src, dst); ``bad_edges`` are drawn in the alarm color (open back-doors)."""
    import plotly.graph_objects as go

    node_colors = node_colors or {}
    fig = go.Figure()
    ann = []
    for (a, b) in list(edges) + list(bad_edges):
        x0, y0 = nodes[a]
        x1, y1 = nodes[b]
        color = BAD if (a, b) in set(map(tuple, bad_edges)) else MUTED
        # shorten the arrow so it lands at the node's edge, not its center
        f = 0.18
        ann.append(dict(
            x=x1 - f * (x1 - x0), y=y1 - f * (y1 - y0),
            ax=x0 + f * (x1 - x0), ay=y0 + f * (y1 - y0),
            xref="x", yref="y", axref="x", ayref="y",
            showarrow=True, arrowhead=3, arrowsize=1.4, arrowwidth=2,
            arrowcolor=color))
    for name, (x, y) in nodes.items():
        fig.add_trace(go.Scatter(
            x=[x], y=[y], mode="markers+text", text=[name],
            textposition="bottom center",
            textfont=dict(size=13, color=INK),
            marker=dict(size=34, color=node_colors.get(
                name, PALETTE.get(name, "#dfe6ea")),
                line=dict(color=INK, width=1.5)),
            showlegend=False, hoverinfo="skip"))
    fig.update_layout(annotations=ann)
    fig.update_xaxes(visible=False, range=[-0.15, 1.15])
    fig.update_yaxes(visible=False, range=[-0.25, 1.2])
    return style(fig, height=height, title=title)


def contraction_chart(ledger: pd.DataFrame, *, title: str | None = None,
                      height: int = 440):
    """CI-width per channel as evidence accumulates. ``ledger`` columns:
    ``stage`` (ordered), ``channel``, ``width``."""
    import plotly.graph_objects as go

    fig = go.Figure()
    for c in ledger["channel"].unique():
        sub = ledger[ledger["channel"] == c]
        fig.add_trace(go.Scatter(
            x=sub["stage"], y=sub["width"], mode="lines+markers", name=c,
            line=dict(color=PALETTE.get(c, MUTED), width=2.5),
            marker=dict(size=9)))
    fig.update_yaxes(title="90% interval width", rangemode="tozero")
    fig.update_xaxes(title="evidence folded in")
    return style(fig, height=height, title=title)


