_TRUTH_WORLDS = {
    "clean", "unobserved_confounding", "confounding_controlled",
    "reverse_causality", "economic_health", "cc_unmarked", "econ_ind",
}


def truth_supported(sc) -> bool:
    """Whether :func:`true_media_term` covers this world's response family."""
    return sc.name.startswith("real") or sc.name in _TRUTH_WORLDS


def true_media_term(sc, channel: str, x) -> np.ndarray:
    """The world's TRUE per-week causal media term for ``channel`` at spend
    path ``x`` (KPI units), reconstructed from the DGP's own response
    constants. Baseline/confounder terms cancel in any spend-perturbation
    difference, so this is exactly what a perfect experiment measures.

    Valid for the additive clean-family worlds (clean / unobserved_confounding
    / confounding_controlled / reverse_causality / economic_health) and the
    ``realistic`` world (whose TV/Video add a mediated awareness path).
    Guarded by :func:`check_truth` — run it in every consuming notebook.
    """
    from mmm_framework.synth import dgp

    if not truth_supported(sc):
        raise ValueError(
            f"true_media_term does not cover world {sc.name!r} "
            "(its DGP has bespoke response constants)"
        )
    x = np.asarray(x, float)
    mx = float(sc.spend[channel].max())
    if sc.name.startswith("real"):
        sat = dgp._logistic_sat(
            dgp._geom_adstock(x / mx, dgp._R_ALPHA[channel]), dgp._R_LAM[channel]
        )
        term = dgp._R_AMP[channel] * sat
        if channel in dgp._R_MEDIATED:  # TV, Video: + b_med * (30 * sat)
            term = term + 4.0 * 30.0 * sat
        return term
    return dgp._AMP[channel] * dgp._logistic_sat(
        dgp._geom_adstock(x / mx, dgp._ALPHA[channel]), dgp._LAM[channel]
    )


def check_truth(sc, rtol: float = 1e-6) -> bool:
    """Self-check: the reconstructed response must reproduce the scenario's own
    sealed answer key (full zero-out lift == true_contribution, per channel).
    If the DGP's constants ever drift, this fails the bake loudly."""
    for c in sc.channels:
        lift = float(true_media_term(sc, c, sc.spend[c].to_numpy(float)).sum())
        truth = float(sc.true_contribution[c])
        if not np.isclose(lift, truth, rtol=rtol):
            raise AssertionError(
                f"truth helper drifted for {sc.name}/{c}: {lift:.3f} != {truth:.3f}"
            )
    return True


def true_readout(sc, channel: str, w0: int, w1: int, *, mult: float = 0.0,
                 se: float | None = None, seed: int = 0) -> dict:
    """What a PERFECT experiment on ``channel`` over weeks ``[w0, w1)`` would
    measure: scale the window's spend by ``mult`` (0 = go dark), difference the
    true response (carryover past the window included), and report the true
    incremental ROAS of the window's spend change. With ``se`` the *observed*
    value adds honest sampling noise — truth + design-implied error, never an
    oracle."""
    x = sc.spend[channel].to_numpy(float)
    x_cf = x.copy()
    x_cf[w0:w1] = x_cf[w0:w1] * mult
    lift = float(
        (true_media_term(sc, channel, x) - true_media_term(sc, channel, x_cf)).sum()
    )
    d_spend = float((x - x_cf).sum())
    roas = lift / d_spend if d_spend else float("nan")
    observed = roas
    if se:
        observed = roas + float(np.random.default_rng(seed).normal(0.0, se))
    return dict(
        channel=channel, w0=w0, w1=w1, mult=mult,
        true_roas=roas, observed=observed, se=se,
        lift=lift, delta_spend=d_spend,
        period=(str(sc.weeks[w0].date()), str(sc.weeks[min(w1, len(sc.weeks)) - 1].date())),
    )


def measurement(sc, channel: str, w0: int, w1: int, *, mult: float = 0.0,
                se: float = 0.12, seed: int = 0, name: str | None = None):
    """A simulated experiment readout as an in-graph ``ExperimentMeasurement``
    (ROAS estimand) plus the readout dict it came from."""
    from mmm_framework.calibration import ExperimentEstimand, ExperimentMeasurement

    r = true_readout(sc, channel, w0, w1, mult=mult, se=se, seed=seed)
    exp = ExperimentMeasurement(
        channel=channel,
        test_period=r["period"],
        value=float(r["observed"]),
        se=float(se),
        estimand=ExperimentEstimand.ROAS,
        distribution="normal",
        name=name or f"{channel}_lift_{r['period'][0]}",
    )
    return exp, r


def true_total_media(sc, spend_df: pd.DataFrame) -> float:
    """The TRUE total media-driven KPI for an arbitrary spend plan (used to
    grade budget allocations against the world, not the model)."""
    return float(
        sum(
            true_media_term(sc, c, spend_df[c].to_numpy(float)).sum()
            for c in sc.channels
        )
    )


