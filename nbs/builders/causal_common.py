def _band(post, var, q=(0.05, 0.5, 0.95)):
    da = post[var]
    qs = da.quantile(list(q), dim=("chain", "draw")).values
    return {"lo": qs[0], "mid": qs[1], "hi": qs[2], "mean": da.mean(("chain", "draw")).values}


def fit_funnel(verbose: bool = True) -> dict:
    """Fit-or-load the brand-funnel structural model (full NUTS, the expensive
    fit of the series). Caches DERIVED tables/arrays, not the live model:
    mediation / pathway / ROAS DataFrames, latent-state bands vs truth, and
    the named structural-parameter posteriors."""
    d = CACHE / "funnel"
    done = d / "meta.json"
    if done.exists():
        out = dict(
            mediation=pd.read_parquet(d / "mediation.parquet"),
            pathways=pd.read_parquet(d / "pathways.parquet"),
            roas=pd.read_parquet(d / "roas.parquet"),
            params=json.load(open(d / "params.json")),
            meta=json.load(open(done)),
        )
        z = np.load(d / "latents.npz")
        out["latents"] = {k: z[k] for k in z.files}
        out["sc"] = funnel_world()
        if verbose:
            print(f"[{BRAND}] loaded cached funnel fit from {d}")
        return out

    if verbose:
        print(f"[{BRAND}] no cache — fitting the structural funnel model "
              f"(4-chain NUTS, ~20-40 min) ...")
    model, sc = build_funnel_model()
    _quiet_pymc()
    t0 = time.perf_counter()
    res = model.fit(**FUNNEL_FIT)
    secs = time.perf_counter() - t0
    post = res.trace.posterior

    mediation = model.get_mediation_effects()
    pathways = model.get_pathway_effects()
    roas = model.get_channel_roas()

    aw = _band(post, "awareness_probability")
    zc = _band(post, "consideration_latent")
    fd = _band(post, "factor_demand")
    latents = dict(
        p_aw_mean=aw["mean"], p_aw_lo=aw["lo"], p_aw_hi=aw["hi"],
        p_aw_true=np.asarray(sc.notes["p_awareness"], float),
        z_cons_mean=zc["mean"], z_cons_lo=zc["lo"], z_cons_hi=zc["hi"],
        z_cons_true=np.asarray(sc.notes["z_consideration"], float),
        demand_mean=fd["mean"], demand_lo=fd["lo"], demand_hi=fd["hi"],
        demand_true=np.asarray(sc.notes["latent_demand"], float),
    )
    named = [
        "awareness_persistence",
        "beta_TV_to_awareness",
        "lambda_awareness_to_consideration",
        "phi_Price_to_consideration",
        "w_demand_to_consideration",
        "gamma_consideration",
    ]
    params = {
        n: dict(
            mean=float(post[n].mean()),
            lo=float(post[n].quantile(0.05)),
            hi=float(post[n].quantile(0.95)),
        )
        for n in named
        if n in post
    }
    meta = dict(
        seconds=round(secs, 1),
        rhat_max=float(res.diagnostics.get("rhat_max", float("nan"))),
        fit=dict(FUNNEL_FIT),
        n_weeks=len(sc.weeks),
        corr_awareness=float(np.corrcoef(latents["p_aw_mean"], latents["p_aw_true"])[0, 1]),
        corr_consideration=float(np.corrcoef(latents["z_cons_mean"], latents["z_cons_true"])[0, 1]),
        corr_demand=float(abs(np.corrcoef(latents["demand_mean"], latents["demand_true"])[0, 1])),
    )
    d.mkdir(parents=True, exist_ok=True)
    mediation.to_parquet(d / "mediation.parquet")
    pathways.to_parquet(d / "pathways.parquet")
    roas.to_parquet(d / "roas.parquet")
    np.savez_compressed(d / "latents.npz", **latents)
    json.dump(params, open(d / "params.json", "w"), indent=2)
    json.dump(meta, open(done, "w"), indent=2)
    if verbose:
        print(f"[{BRAND}] funnel fit in {secs/60:.0f} min "
              f"(rhat_max={meta['rhat_max']:.3f}) — cached to {d}")
    return dict(mediation=mediation, pathways=pathways, roas=roas,
                params=params, meta=meta, latents=latents, sc=sc)


