def _quiet_pymc():
    """Silence pymc/arviz advisory chatter before sampling. CRITICAL (not
    ERROR): pymc logs sampler/ESS advisories at ERROR level, and the child
    loggers re-set their own level at import."""
    import logging as _logging

    for _n in (
        "pymc",
        "pymc.sampling",
        "pymc.sampling.mcmc",
        "pymc.stats",
        "pymc.stats.convergence",
        "numpyro",
        "jax",
        "arviz",
        "pytensor",
    ):
        _lg = _logging.getLogger(_n)
        _lg.setLevel(_logging.CRITICAL)
        _lg.propagate = False


def write_artifact(name: str, payload: dict) -> str:
    """Write a JSON answer-sheet artifact the docs pages can quote."""
    ARTIFACTS.mkdir(parents=True, exist_ok=True)
    path = ARTIFACTS / name

    def _safe(o):
        if isinstance(o, dict):
            return {str(k): _safe(v) for k, v in o.items()}
        if isinstance(o, (list, tuple)):
            return [_safe(v) for v in o]
        if isinstance(o, (np.floating, np.integer)):
            return float(o)
        if isinstance(o, np.ndarray):
            return [float(v) for v in o.ravel()]
        return o

    json.dump(_safe(payload), open(path, "w"), indent=2)
    return str(path)


