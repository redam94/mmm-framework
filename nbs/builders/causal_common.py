LATENT_FIT = dict(draws=400, tune=800, chains=4, target_accept=0.9, random_seed=7)


def _garden_path() -> Path:
    return Path(__file__).resolve().parents[2] / "examples" / "garden_models"


def fit_latent(verbose: bool = True) -> dict:
    """Fit-or-load the joint latent-factor MMM on the economic-health world
    (rung C of nb04's adjustment ladder). Caches loadings / ROI / factor-path
    derived tables. NUTS only — MAP is unstable for this ~150-param model."""
    d = CACHE / "latent"
    done = d / "meta.json"
    if done.exists():
        out = dict(
            loadings=pd.read_parquet(d / "loadings.parquet"),
            roi=pd.read_parquet(d / "roi.parquet"),
            grade=pd.read_parquet(d / "grade.parquet"),
            meta=json.load(open(done)),
        )
        z = np.load(d / "factor.npz")
        out["factor"] = {k: z[k] for k in z.files}
        out["sc"] = world("economic_health")
        if verbose:
            print(f"[{BRAND}] loaded cached latent-factor fit from {d}")
        return out

    import sys

    gp = str(_garden_path())
    if gp not in sys.path:
        sys.path.insert(0, gp)
    from latent_factor_mmm import LatentFactorMMM, economic_health_dataset

    from mmm_framework.config import ModelConfig
    from mmm_framework.model import TrendConfig
    from mmm_framework.model.trend_config import TrendType
    from mmm_framework.reporting.helpers import compute_roi_with_uncertainty

    if verbose:
        print(f"[{BRAND}] no cache — fitting the joint latent-factor MMM "
              f"(4-chain NUTS, ~10-30 min) ...")
    dataset, sc = economic_health_dataset(seed=WORLD_SEEDS["economic_health"])
    model = LatentFactorMMM(dataset, ModelConfig(), TrendConfig(type=TrendType.NONE))
    _quiet_pymc()
    t0 = time.perf_counter()
    res = model.fit(**LATENT_FIT)
    secs = time.perf_counter() - t0
    post = res.trace.posterior if hasattr(res, "trace") else model._trace.posterior

    loadings = model.factor_loadings_summary()
    roi = compute_roi_with_uncertainty(model, hdi_prob=0.9)
    g = grade(sc, model)  # same counterfactual estimand as the A/B rung fits
    fb = _band(post, "economic_health")
    factor = dict(
        mean=fb["mean"], lo=fb["lo"], hi=fb["hi"],
        true=np.asarray(sc.notes["latent_econ"], float),
    )
    diags = getattr(res, "diagnostics", {}) or {}
    meta = dict(
        seconds=round(secs, 1),
        rhat_max=float(diags.get("rhat_max") or float("nan")),
        fit=dict(LATENT_FIT),
        corr_factor=float(abs(np.corrcoef(factor["mean"], factor["true"])[0, 1])),
        true_loadings=dict(sc.notes["true_loadings"]),
    )
    d.mkdir(parents=True, exist_ok=True)
    loadings.to_parquet(d / "loadings.parquet")
    roi.to_parquet(d / "roi.parquet")
    g.to_parquet(d / "grade.parquet")
    np.savez_compressed(d / "factor.npz", **factor)
    json.dump(meta, open(done, "w"), indent=2)
    if verbose:
        print(f"[{BRAND}] latent-factor fit in {secs/60:.0f} min "
              f"(factor corr={meta['corr_factor']:.2f}) — cached to {d}")
    return dict(loadings=loadings, roi=roi, grade=g, factor=factor, meta=meta, sc=sc)


