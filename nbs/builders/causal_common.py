def _fit_dir(key: str) -> Path:
    return CACHE / f"fit_{key}"


def _model_config(draws: int, tune: int, chains: int):
    from mmm_framework.config import InferenceMethod, ModelConfig

    return ModelConfig(
        inference_method=InferenceMethod.BAYESIAN_NUMPYRO,
        n_draws=draws,
        n_tune=tune,
        n_chains=chains,
        use_parametric_adstock=True,
        optim_seed=0,
    )


def grade(sc, mmm, *, hdi_prob: float = 0.9, seed: int = 0) -> pd.DataFrame:
    """Grade every channel against the sealed answer key, on the model's own
    estimand (counterfactual zero-out contribution) — apples to apples."""
    contrib = mmm.compute_counterfactual_contributions(
        compute_uncertainty=True, hdi_prob=hdi_prob, random_seed=seed
    )
    rows = []
    for c in sc.channels:
        t = float(sc.true_contribution[c])
        e = float(contrib.total_contributions[c])
        lo = float(contrib.contribution_hdi_low[c])
        hi = float(contrib.contribution_hdi_high[c])
        spend = float(sc.spend[c].sum())
        rows.append(
            {
                "channel": c,
                "true": t,
                "est": e,
                "lo": lo,
                "hi": hi,
                "rel_err": (e - t) / abs(t),
                "covered": bool(lo <= t <= hi),
                "true_roas": float(sc.true_roas[c]),
                "est_roas": e / spend,
                "roas_lo": lo / spend,
                "roas_hi": hi / spend,
            }
        )
    return pd.DataFrame(rows).set_index("channel")


def fit_world(key: str, *, draws: int = DRAWS, tune: int = TUNE,
              chains: int = CHAINS, trend: str = "linear",
              verbose: bool = True) -> dict:
    """Fit-or-load the observational MMM for a named (world x controls) fit.

    Returns ``dict(model, sc, panel, grade, diag)``. The fit is a fast-but-real
    numpyro NUTS posterior; the grade table (counterfactual contributions vs
    the sealed key) is computed once at fit time and cached alongside.
    ``trend="none"`` drops the linear trend (cached separately) — used on the
    economic-health world, whose only slow signal IS the latent factor.
    """
    from mmm_framework.serialization import MMMSerializer

    d = _fit_dir(key if trend == "linear" else f"{key}_notrend")
    if (d / "model").exists() and (d / "panel.pkl").exists():
        panel = cloudpickle.load(open(d / "panel.pkl", "rb"))
        model = MMMSerializer.load(str(d / "model"), panel=panel, rebuild_model=True)
        g = pd.read_parquet(d / "grade.parquet")
        diag = json.load(open(d / "diag.json"))
        sc = scenario_for(key)
        if verbose:
            print(f"[{BRAND}] loaded cached fit '{key}' from {d}")
        return dict(model=model, sc=sc, panel=panel, grade=g, diag=diag)

    from mmm_framework.model import BayesianMMM, TrendConfig, TrendType

    sc = scenario_for(key)
    if truth_supported(sc):
        check_truth(sc)
    panel = sc.panel()
    if verbose:
        print(f"[{BRAND}] no cache — fitting '{key}' "
              f"({len(sc.channels)} channels, {len(sc.weeks)} weeks) ...")
    model = BayesianMMM(
        panel,
        _model_config(draws, tune, chains),
        TrendConfig(type=TrendType.LINEAR if trend == "linear" else TrendType.NONE),
    )
    _quiet_pymc()
    t0 = time.perf_counter()
    fit = model.fit(random_seed=SEED, progressbar=False)
    secs = time.perf_counter() - t0
    g = grade(sc, model)
    diag = dict(
        seconds=round(secs, 1),
        rhat_max=float(fit.diagnostics.get("rhat_max", float("nan"))),
        divergences=int(fit.diagnostics.get("divergences", 0) or 0),
        ess_bulk_min=float(fit.diagnostics.get("ess_bulk_min", float("nan"))),
        draws=draws, tune=tune, chains=chains,
    )
    d.mkdir(parents=True, exist_ok=True)
    MMMSerializer.save(model, str(d / "model"), save_trace=True, compress=True)
    cloudpickle.dump(panel, open(d / "panel.pkl", "wb"))
    g.to_parquet(d / "grade.parquet")
    json.dump(diag, open(d / "diag.json", "w"), indent=2)
    if verbose:
        print(f"[{BRAND}] fit '{key}' in {secs:.0f}s "
              f"(rhat_max={diag['rhat_max']:.3f}) — cached to {d}")
    return dict(model=model, sc=sc, panel=panel, grade=g, diag=diag)


def fit_calibrated(key: str, experiments: list, *, draws: int = DRAWS,
                   tune: int = TUNE, chains: int = CHAINS, seed: int | None = None,
                   verbose: bool = True) -> dict:
    """Reload the cached baseline for ``key``, attach in-graph experiment
    likelihoods, and refit (fresh model object each call — fast). Returns
    ``dict(model, sc, grade, diag)``."""
    base = fit_world(key, verbose=False)
    model, sc = base["model"], base["sc"]
    model.add_experiment_calibration(list(experiments))
    _quiet_pymc()
    t0 = time.perf_counter()
    fit = model.fit(draws=draws, tune=tune, chains=chains,
                    random_seed=SEED + 1 if seed is None else seed,
                    progressbar=False)
    secs = time.perf_counter() - t0
    g = grade(sc, model)
    if verbose:
        chs = ", ".join(sorted({e.channel for e in experiments}))
        print(f"[{BRAND}] refit '{key}' with {len(experiments)} experiment(s) "
              f"on [{chs}] in {secs:.0f}s")
    diag = dict(seconds=round(secs, 1),
                rhat_max=float(fit.diagnostics.get("rhat_max", float("nan"))))
    return dict(model=model, sc=sc, grade=g, diag=diag)


